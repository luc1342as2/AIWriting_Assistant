  function detectLang() {
    try {
      const saved = localStorage.getItem(LANG_STORAGE);
      if (saved && t[saved]) return saved;
      const nav = navigator.language || navigator.userLanguage || "";
      const code = nav.split("-")[0].toLowerCase();
      if (t[code]) return code;
      const pref = (navigator.languages || [nav])[0];
      if (pref) {
        const pCode = pref.split("-")[0].toLowerCase();
        if (t[pCode]) return pCode;
      }
    } catch (_) {}
    return "en";
  }

  function getLang() {
    return window._currentLang || "en";
  }

  function setLang(lang) {
    if (!t[lang]) lang = "en";
    window._currentLang = lang;
    try { localStorage.setItem(LANG_STORAGE, lang); } catch (_) {}
    document.documentElement.lang = lang === "zh" ? "zh-CN" : lang;
    document.documentElement.setAttribute("dir", lang === "ar" ? "rtl" : "ltr");
  }

  function tr(key) {
    return (t[getLang()] || t.en)[key] ?? (t.en[key] ?? key);
  }

  function getLangName(targetLang) {
    return (langName[getLang()] || langName.en)[targetLang] || targetLang;
  }

  function applyTranslations() {
    const lang = getLang();
    const strings = t[lang] || t.en;
    const el = (id) => document.getElementById(id);
    const q = (sel) => document.querySelector(sel);
    const qa = (sel) => document.querySelectorAll(sel);

    const set = (id, key) => { const e = el(id); if (e && strings[key]) e.textContent = strings[key]; };
    const setPlaceholder = (id, key) => { const e = el(id); if (e && strings[key]) e.placeholder = strings[key]; };
    const setTitle = (id, key) => { const e = el(id); if (e && strings[key]) e.title = strings[key]; };

    set("i18n-badge", "badge");
    set("i18n-tagline", "tagline");
    qa("[data-i18n]").forEach((e) => {
      const k = e.getAttribute("data-i18n");
      if (strings[k]) e.textContent = strings[k];
    });
    qa("[data-i18n-placeholder]").forEach((e) => {
      const k = e.getAttribute("data-i18n-placeholder");
      if (strings[k]) e.placeholder = strings[k];
    });
    setPlaceholder("editor", "placeholder");
    set("status", "ready");
    const denyEl = el("terms-deny-msg");
    if (denyEl) denyEl.textContent = strings.termsDeny;

    const toggle = el("btn-lang-toggle");
    const dropdown = el("i18n-btn");
    const currentSpan = q(".lang-current");
    if (currentSpan) currentSpan.textContent = getLangName(lang);
    if (dropdown) {
      const others = SUPPORTED.filter((l) => l !== lang);
      dropdown.innerHTML = others.map((l) => `<button type="button" class="lang-opt" data-lang="${l}">${getLangName(l)}</button>`).join("");
      dropdown.setAttribute("aria-hidden", "true");
      qa(".lang-opt").forEach((btn) => {
        btn.onclick = () => {
          setLang(btn.dataset.lang);
          applyTranslations();
          if (toggle) { toggle.setAttribute("aria-expanded", "false"); dropdown.classList.remove("open"); }
          if (typeof window.onLangChange === "function") window.onLangChange();
        };
      });
    }
    if (toggle && dropdown) {
      toggle.onclick = (e) => {
        e.stopPropagation();
        dropdown.classList.toggle("open");
        toggle.setAttribute("aria-expanded", dropdown.classList.contains("open"));
        dropdown.setAttribute("aria-hidden", !dropdown.classList.contains("open"));
      };
      dropdown.onclick = (e) => e.stopPropagation();
      if (!dropdown._clickOutBound) {
        dropdown._clickOutBound = true;
        document.addEventListener("click", () => {
          if (dropdown.classList.contains("open")) {
            dropdown.classList.remove("open");
            toggle.setAttribute("aria-expanded", "false");
            dropdown.setAttribute("aria-hidden", "true");
          }
        });
      }
    }
    qa("[data-i18n-title]").forEach((e) => {
      const k = e.getAttribute("data-i18n-title");
      if (strings[k]) e.title = strings[k];
    });
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init);
  } else {
    init();
  }
  function init() {
    setLang(detectLang());
    applyTranslations();
  }

  return {
    SUPPORTED,
    detectLang,
    getLang,
    setLang,
    tr,
    getLangName,
    applyTranslations,
  };
